public class swapping {
  public static void main (String args[]){
    // swap // value exchange
    int a = 10;
    int b = 5 ;

    // * swapping
    int temp = a ;
    a = b ;
    b = temp ;

    System.out.println("the value of a is :" + a );
    System.out.println("the value of b is :" + b );

  }
}
